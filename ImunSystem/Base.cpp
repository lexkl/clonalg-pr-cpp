#include "Base.h"

/*****************************************************************************/

Base::Base()
{
}


/*****************************************************************************/


Base::Base( int _aMatrix[Row_Number][Column_Number] )
{
	for (int i = 0; i < Row_Number; i++)
	{
		for (int j = 0; j < Column_Number; j++)
		{
			m_aMatrix[i][j] = _aMatrix[i][j];
		}
	}
}


/*****************************************************************************/


Base::~Base()
{
}


